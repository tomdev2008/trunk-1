public class JsonRetEntity<D> {
    private Page page;
    public AdvertEntity advert;
    private D data;
    private ResponseResult error;

    public D getData() {
        return data;
    }

    public void setData(D data) {
        this.data = data;
    }

    public Page getPage() {
        return page;
    }

    public void setPage(Page page) {
        this.page = page;
    }

    public ResponseResult getError() {
        return error;
    }

    public void setError(ResponseResult error) {
        this.error = error;
    }

    @Override
    public String toString() {
        return "JsonRetEntity{" +
                "page=" + page +
                ", advert=" + advert +
                ", data=" + data +
                ", error=" + error +
                '}';
    }
}
